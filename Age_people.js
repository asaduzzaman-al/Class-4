
let peopleage = (name, year,stage) =>{
    return `Hi ${ name }. You are ${2021-year}years old. Therefore you are a ${stage}. `;
}
console.log(peopleage('Jack', 2009, 'Teenager'));
console.log(peopleage('Arman', 2003, 'Children'));
console.log(peopleage('Babor', 2000, 'Young Man'));
console.log(peopleage('Nawaz', 1960, 'Oldman'));
